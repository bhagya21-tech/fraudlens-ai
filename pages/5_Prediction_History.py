import streamlit as st
import pandas as pd
import os
from src.utils import load_css

# ==========================================
# Page Config
# ==========================================

st.set_page_config(
    page_title="Prediction History",
    page_icon="📜",
    layout="wide"
)

load_css()

# ==========================================
# Hero Banner
# ==========================================

st.markdown("""
<div class="hero">

<h1>📜 Prediction History</h1>

<p>

Track all previous predictions generated by FraudLens AI.

</p>

</div>
""", unsafe_allow_html=True)

# ==========================================
# Load History
# ==========================================

history_file = "data/prediction_history.csv"

if not os.path.exists(history_file):

    pd.DataFrame(

        columns=[
            "Timestamp",
            "Prediction",
            "Confidence",
            "Job Title",
            "Industry"
        ]

    ).to_csv(

        history_file,

        index=False

    )

history = pd.read_csv(history_file)

history["prediction"] = history["Prediction"].astype(str)

# ==========================================
# Empty State
# ==========================================

if history.empty:

    st.info("No predictions have been made yet.")

    st.stop()

# ==========================================
# Dashboard Metrics
# ==========================================

total_predictions = len(history)

real_jobs = history["Prediction"].str.contains(
    "Real",
    case=False,
    na=False
).sum()

fake_jobs = history["Prediction"].str.contains(
    "Fake",
    case=False,
    na=False
).sum()

avg_confidence = round(
    history["Confidence"].mean(),
    2
)

c1, c2, c3, c4 = st.columns(4)

with c1:
    st.metric(
        "Total Predictions",
        total_predictions
    )

with c2:
    st.metric(
        "Real Jobs",
        real_jobs
    )

with c3:
    st.metric(
        "Fake Jobs",
        fake_jobs
    )

with c4:
    st.metric(
        "Average Confidence",
        f"{avg_confidence}%"
    )

st.divider()

# ==========================================
# Search
# ==========================================

search = st.text_input(
    "🔍 Search Job Title"
)

if search:

    history = history[
        history["Job Title"]
        .str.contains(
            search,
            case=False,
            na=False
        )
    ]

# ==========================================
# Prediction Cards
# ==========================================

st.subheader("📋 Recent Predictions")

for _, row in history.iloc[::-1].iterrows():

    prediction = row["Prediction"]

    confidence = row["Confidence"]

    if "Fake" in prediction:

        color = "#EF4444"

        icon = "🚨"

    else:

        color = "#22C55E"

        icon = "✅"

    st.markdown(
        f"""
<div style="
background:white;
padding:20px;
border-radius:18px;
margin-bottom:18px;
border-left:8px solid {color};
box-shadow:0px 5px 12px rgba(0,0,0,.08);
">

<h4>{icon} {row['Job Title']}</h4>

<b>Prediction:</b> {prediction}

<br>

<b>Confidence:</b> {confidence}%

<br>

<b>Industry:</b> {row.get('Industry','N/A')}

<br>

<b>Timestamp:</b> {row['Timestamp']}

</div>
""",
        unsafe_allow_html=True
    )

st.divider()

# ==========================================
# History Table
# ==========================================

st.subheader("📊 Prediction Table")

st.dataframe(

    history,

    use_container_width=True,

    hide_index=True

)

st.divider()

# ==========================================
# Download History
# ==========================================

csv = history.to_csv(index=False)

st.download_button(

    "📥 Download Prediction History",

    csv,

    "prediction_history.csv",

    "text/csv",

    use_container_width=True

)

# ==========================================
# Clear History
# ==========================================

if st.button(
    "🗑 Clear History",
    use_container_width=True
):

    pd.DataFrame(

        columns=[
            "Timestamp",
            "Prediction",
            "Confidence",
            "Job Title",
            "Industry"
        ]

    ).to_csv(

        history_file,

        index=False

    )

    st.success("Prediction history cleared.")

    st.rerun()

# ==========================================
# Footer
# ==========================================

st.divider()

st.markdown("""

<div class="footer">

<b>FraudLens AI</b>

<br>

Prediction History Dashboard

<br><br>

Built using Python • Machine Learning • Streamlit

<br><br>

© 2026 Bhagyashri Raut

</div>

""", unsafe_allow_html=True)